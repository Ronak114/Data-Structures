public class a56_bitwiseOperator {
    public static void main(String args[]){
        //Binary AND (&)
        System.out.println(5 & 6);
        //Binary OR(|)
        System.out.println(5 | 6);
        //Binary XOR(^)
        System.out.println(5 ^ 6);
        //Binary One's complement(~)
        System.out.println(~ 5);
        /*********REMEMBER********** */
        System.out.println(~ 0);
         //Binary Left Shift(<<)
        System.out.println(5<<2);
         //Binary Right Shift(>>)
        System.out.println(6>>1);
    }
}
